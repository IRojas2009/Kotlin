package `1_Tipos_de_variables`

fun main() {
    //Variable inmutable de tipo entero
    val edad: Int
    edad = 48
    //Variable inmutable tipo decimal
    val sueldo: Float
    sueldo = 1200.55f //siempre f despues de valor numerico con denominacion "float"
    //Variable inmutable tipo decimal
    val total: Double
    total = 70000.24
    //Variable inmutable de tipo alfanumerico
    val titulo: String
    titulo = "Sistema de ventas"

    //Variable mutable, las variables mutables empiezan con "var"
    var mes: Int
    mes= 1
    //(Lineas de codigo)
    mes= 2

    //El valor "Char" almacena valores de un solo caracter
    var continua: Char = 's'

    //Variable "Boolean" significa verdadero o falso
    var fin: Boolean = false

    print("Isaac")
    print("Gabriel ")

    println("Rojas ")
    println("Villegas")

}